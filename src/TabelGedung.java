    private void hapusGedungActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hapusGedungActionPerformed
        DefaultTableModel model = (DefaultTableModel)tabelGedung.getModel();
        int row = tabelGedung.getSelectedRow();

        Gedung gd = new Gedung().getById(Integer.parseInt(model.getValueAt(row, 0).toString()));
        gd.delete();
        kosongkanFormGedung();
        tampilkanGedung();

    }//GEN-LAST:event_hapusGedungActionPerformed

    private void tambahGedungActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tambahGedungActionPerformed
        Gedung gd = new Gedung();
        gd.setId_gedung(Integer.parseInt(gedung.getText()));
        gd.setR_ganti(Integer.parseInt(rGanti.getText()));
        gd.setNama(namaGedung.getText());
        gd.setAlamat(alamat.getText());
        gd.save();
        gedung.setText(Integer.toString(gd.getId_gedung()));
        tampilkanGedung();
        //        tampil_combo();

    }//GEN-LAST:event_tambahGedungActionPerformed

