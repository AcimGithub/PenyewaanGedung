    public void tampilkanGedung() {
        String [] kolom ={"ID_Gedung","Ruang Ganti","Nama Gedung","Alamat"};
        ArrayList<Gedung> list = new Gedung().getAll();
        Object rowData[] = new Object[4];
        
        tabelGedung.setModel(new DefaultTableModel(new Object[][] {}, kolom));
        
        for(Gedung gd : list) {
            rowData[0] = gd.getId_gedung();
            rowData[1] = gd.getR_ganti();
            rowData[2] = gd.getNama();
            rowData[3] = gd.getAlamat();
            
            ((DefaultTableModel)tabelGedung.getModel()).addRow(rowData);
        }
    }
    
