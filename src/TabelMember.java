    private void saveMemberActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveMemberActionPerformed
        Member mb = new Member();
        mb.setId_member(Integer.parseInt(idmember.getText()));
        mb.setNama(namatim.getText());
        mb.setNo_hp(nomor.getText());
        mb.save();
        idmember.setText(Integer.toString(mb.getId_member()));
        tampilkanMember();
    }//GEN-LAST:event_saveMemberActionPerformed

    private void delMemberActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_delMemberActionPerformed
        DefaultTableModel model = (DefaultTableModel)tabelMember.getModel();
        int row = tabelMember.getSelectedRow();

        Member mb = new Member().getById(Integer.parseInt(model.getValueAt(row, 0).toString()));
        mb.delete();
        kosongkanFormMember();
        tampilkanMember();
    }//GEN-LAST:event_delMemberActionPerformed

    private void tabelMemberMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelMemberMouseClicked
        DefaultTableModel model = (DefaultTableModel)tabelMember.getModel();
        int row = tabelMember.getSelectedRow();

        idmember.setText(model.getValueAt(row, 0).toString());
        namatim.setText(model.getValueAt(row, 1).toString());
        nomor.setText(model.getValueAt(row, 2).toString());

    }//GEN-LAST:event_tabelMemberMouseClicked

