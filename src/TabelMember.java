    public void tampilkanMember() {
        String [] kolom ={"ID_Member","Nama Tim","Nomor HP"};
        ArrayList<Member> list = new Member().getAll();
        Object rowData[] = new Object[3];
        
        tabelMember.setModel(new DefaultTableModel(new Object[][] {}, kolom));
        
        for(Member mb : list) {
            rowData[0] = mb.getId_member();
            rowData[1] = mb.getNama();
            rowData[2] = mb.getNo_hp();

            
            ((DefaultTableModel)tabelMember.getModel()).addRow(rowData);
        }
    }

